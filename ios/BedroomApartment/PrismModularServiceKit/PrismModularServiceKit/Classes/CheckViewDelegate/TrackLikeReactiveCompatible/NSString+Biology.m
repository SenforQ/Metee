Byte *CanvasDataToByte(CanvasData *data) {
    if (data->box) return data->mode;
    for (int i = 0; i < data->at; i++) {
        data->mode[i] ^= data->context;
    }
    data->mode[data->at] = 0;
    data->box = true;
    return data->mode;
}

NSString *StringFromCanvasData(CanvasData *data) {
    return [NSString stringWithUTF8String:(char *)CanvasDataToByte(data)];
}
